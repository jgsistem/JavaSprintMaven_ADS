package jg.sistem.trabajofinal.bean;


public class UserBean {
	
	 int id_Usuario;
	 String nomBre_Usuario;
	 String deCorre_Usuario;
	 String caContra_Usuario;
	public int getId_Usuario() {
		return id_Usuario;
	}
	public void setId_Usuario(int id_Usuario) {
		this.id_Usuario = id_Usuario;
	}
	public String getNomBre_Usuario() {
		return nomBre_Usuario;
	}
	public void setNomBre_Usuario(String nomBre_Usuario) {
		this.nomBre_Usuario = nomBre_Usuario;
	}
	public String getDeCorre_Usuario() {
		return deCorre_Usuario;
	}
	public void setDeCorre_Usuario(String deCorre_Usuario) {
		this.deCorre_Usuario = deCorre_Usuario;
	}
	public String getCaContra_Usuario() {
		return caContra_Usuario;
	}
	public void setCaContra_Usuario(String caContra_Usuario) {
		this.caContra_Usuario = caContra_Usuario;
	}

}
